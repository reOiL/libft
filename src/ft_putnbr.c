#include <libft.h>

void	ft_putnbr(int nb)
{
	long long ll;

	ll = nb;
	if (ll == 0)
	{
		ft_putchar('0');
		return ;
	}
	if (ll < 0)
	{
		ft_putchar('-');
		ll *= -1;
	}
	if (ll / 10 != 0)
		ft_putnbr(ll / 10);
	ft_putchar('0' + ll % 10);
}
