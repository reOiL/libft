#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len1;
	size_t	len2;
	char	*ret;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	len1 = ft_strlen(s1);
	len2 = ft_strlen(s2);
	ret = ft_strnew(len1 + len2);
	if (ret == NULL)
		return (NULL);
	ft_strcpy(ret, s1);
	ft_strcpy(ret + len1, s2);
	return (ret);
}
